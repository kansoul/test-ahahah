export const updateProfileValidator = validate(
  checkSchema(
    {
      name: {
        notEmpty: {
          errorMessage: USERS_MESSAGES.NAME_IS_REQUIRED
        },
        isString: {
          errorMessage: USERS_MESSAGES.NAME_MUST_BE_A_STRING
        },
        isLength: {
          options: {
            min: 1,
            max: 100
          },
          errorMessage: USERS_MESSAGES.NAME_LENGTH_MUST_BE_FROM_1_TO_100
        },
        trim: true
      },
      email: {
        notEmpty: {
          errorMessage: USERS_MESSAGES.EMAIL_IS_REQUIRED
        },
        isEmail: {
          errorMessage: USERS_MESSAGES.EMAIL_IS_INVALID
        },
        trim: true,
        custom: {
          options: async (value, { req }) => {
            const { user_id } = req.decoded_authorization as TokenPayload

            const user = await User.findById(user_id)

            if (user && user.email !== value) {
              const isExitEmail = await usersService.checkEmailExist(value)
              if (isExitEmail) {
                throw new Error(USERS_MESSAGES.EMAIL_ALREADY_EXISTS)
              }
            }

            return true
          }
        }
      },
      date_of_birth: {
        notEmpty: {
          errorMessage: USERS_MESSAGES.DATE_OF_BIRTH_IS_REQUIRED
        },
        isISO8601: {
          options: {
            strict: true,
            strictSeparator: true
          },
          errorMessage: USERS_MESSAGES.DATE_OF_BIRTH_MUST_BE_ISO8601
        }
      },
      phoneNumber: {
        custom: {
          options: (values) => {
            if (!!values && !REGEX.ONLY_NUMBER.test(values)) return false
            return true
          },
          errorMessage: USERS_MESSAGES.PHONE_NUMBER
        }
      }
    },
    ['body']
  )
)

